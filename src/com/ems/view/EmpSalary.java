    private void btn_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_saveActionPerformed
        // TODO add your handling code here:
        if (txt_id.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Please enter a Employee ID");

        } else {
            int p = JOptionPane.showConfirmDialog(null, "Are you sure you want to save record?", "Add Record", JOptionPane.YES_NO_OPTION);
            if (p == 0) {
                try {
                    String selectQuery = "Select * from empsalary where Emp_id='" + txt_id.getText() + "' and month='" + cmb_month.getMonth() + "' ";
                    pst = conn.prepareStatement(selectQuery);
                    rs = pst.executeQuery();

                    if (rs.next() == true) {
                        JOptionPane.showMessageDialog(null, "Already Calculated Salary for Employee");
                    } else {

                        try {

                            String sql = "insert into empsalary (Emp_id,name,nic,doj,department,designation,bsalary,ot_hours,medical,bonus,other,ot_rate,total_overtime,attendance_bonus,total_allowance,deduction_amount,reason,late_days,late_days_amount,month) values (?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?)";
                            pst = conn.prepareStatement(sql);

                            pst.setString(1, txt_id.getText());
                            pst.setString(2, txt_name.getText());
                            pst.setString(3, txt_nic.getText());
                            pst.setString(4, txt_doj.getText());
                            pst.setString(5, txt_dep.getText());
                            pst.setString(6, txt_desig.getText());
                            pst.setString(7, txt_bsalary.getText());
                            pst.setString(8, txt_hw.getText());
                            pst.setString(9, txt_med.getText());
                            pst.setString(10, txt_bonus.getText());
                            pst.setString(11, txt_other.getText());
                            pst.setString(12, txt_per.getText());
                            pst.setString(13, txt_total_overtime.getText());
                            pst.setString(14, txt_att.getText());
                            pst.setString(15, lbl_atotal.getText());
                            pst.setString(16, lbl_dtotal.getText());
                            pst.setString(17, txt_reason.getText());
                            pst.setString(18, txt_late.getText());
                            pst.setString(19, txt_tlate.getText());
                            pst.setInt(20, cmb_month.getMonth());

                            pst.execute();

                            JOptionPane.showMessageDialog(null, "Data is saved successfully");

                        } catch (Exception e) {
                            JOptionPane.showMessageDialog(null, "Invalid Information");
                        } finally {

                            try {
                                rs.close();
                                pst.close();

                            } catch (Exception e) {

                            }
                        }

                    }

                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, e);
                }
            }
        }
    }//GEN-LAST:event_btn_saveActionPerformed

