    private void txt_calActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txt_calActionPerformed
        // TODO add your handling code here:
        try {
            int salary = Integer.parseInt(txt_bsalary.getText());
            int overtime = Integer.parseInt(txt_hw.getText());
            int late = Integer.parseInt(txt_late.getText());

            double eight = 8;
            double days = 25;
            double dbop = 0;
            double dlate = 0;
            double overtimeRate = 1.5;

            //calculate the total hours of overtime
            double Total_Overtime = overtime * overtimeRate;
            String x = String.valueOf(Total_Overtime);
            txt_total_overtime.setText(x);

            //calculate overall overtime 
            dbop = salary / days / eight;
            String s = String.valueOf(dbop);
            txt_per.setText(s);

            int med = Integer.parseInt(txt_med.getText());
            int bonus = Integer.parseInt(txt_bonus.getText());
            int other = Integer.parseInt(txt_other.getText());
            int att = Integer.parseInt(txt_att.getText());
            int f = med + bonus + other + att;
            double calc = Total_Overtime * dbop + f;
            String c = String.valueOf(calc);
            lbl_atotal.setText(c);
            double sal_with_allowances = salary + calc;

            //Calculate late
            dlate = (salary / days / eight) / 2;
            double Total_Late = late * dlate;
            String l = String.valueOf(Total_Late);
            txt_tlate.setText(l);

            double sal_with_deduct = sal_with_allowances - Total_Late;
            double epf = sal_with_deduct / 100 * 8;
            double etf = sal_with_deduct / 100 * 12;
            double sal_epf = sal_with_deduct - epf;
            String epf1 = String.valueOf(epf);
            String etf1 = String.valueOf(etf);
            txt_epf.setText(epf1);
            txt_etf.setText(etf1);

            double deduct = Total_Late + epf;
            lbl_dtotal.setText(String.valueOf(deduct));

            lbl_sal.setText(String.valueOf(sal_epf));

            if (r_percentage.isSelected() == true) {
                double percentage = Integer.parseInt(txt_percentage.getText());
                //calculate the total hours of overtime
                double total_percentage_deduction = salary / 100 * percentage;
                String y = String.valueOf(total_percentage_deduction);
                double sal = sal_with_allowances - (total_percentage_deduction + Total_Late + epf);
                lbl_dtotal.setText(y);
                lbl_sal.setText(String.valueOf(sal));
            }

            if (r_amount2.isSelected() == true) {
                int deduction = Integer.parseInt(txt_amount.getText());
                //calculate the total hours of overtime
                double total_deduction = sal_with_allowances - (deduction + Total_Late + epf);
                String t = String.valueOf(total_deduction);

                lbl_sal.setText(t);
                lbl_dtotal.setText(String.valueOf(deduction));

            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Please check out values");

        }
    }//GEN-LAST:event_txt_calActionPerformed

