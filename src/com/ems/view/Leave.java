    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String tmp = (String) cmb_id.getSelectedItem();
        SimpleDateFormat dateFormat=new SimpleDateFormat("yyyy-MM-dd");
        String Date=dateFormat.format(jDateChooser1.getDate());
        String leave = "leave";

        try {
            String sql1 = "select * from attendance Where emp_id ='" + tmp + "' and date='" + Date + "' ";
            pst = conn.prepareStatement(sql1);
            rs = pst.executeQuery();

            if (rs.next() == true) {
                JOptionPane.showMessageDialog(this, "You are already booked this date");
            } else {
                String reg = "insert into attendance (emp_id,date,status) values ('"+ tmp +"','"+ Date +"','"+ leave +"')  ";
                pst = conn.prepareStatement(reg);
                pst.execute();

                JOptionPane.showMessageDialog(this, "Leave booked Successfully");

            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Please select a Employee ID");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

